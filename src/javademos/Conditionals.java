package javademos;

public class Conditionals {

	public static void main(String[] args) {
		int sayi = 23;
		if (sayi < 20) {
			System.out.println("Sayi 20den küçüktür");
		} 
		else if (sayi >20) {
			System.out.println("Sayi 20den büyüktür");
		}
		else {
			System.out.println("Sayi 20den eşittir");
		}

	}

}
